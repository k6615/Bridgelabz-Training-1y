class operators
{
	public static void main(String...args)
	{
		int a = 10;
		System.out.println(a);
		a += 10;
		System.out.println(a);
		a -= 10;
		System.out.println(a);
		
	// if else 	
		
		int age = 20;
		if(age>=18)
		{
		System.out.println("you are allowed");
		}
		else
		{
			System.out.println("you are not allowed");
		}
		
	//   Unary operator
	
		int value = 10;
		System.out.println(value++);
		System.out.println(++value);
		System.out.println(value--);
		System.out.println(--value);
	}
}

		
		
		
		
		
